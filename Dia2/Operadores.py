x = 6 
y = 2
z = 7

print(f"{x} mas {y} es igual a {x + y}")
print(f"{x} menos {y} es igual a {x - y}")
print(f"{x} por {y} es igual a {x * y}")
print(f"{x} dividido {y} es igual a {x / y}")

#Division al piso si 7/2 = 3.5 con esta division solo obtendriamos el 3
print(f"{z} dividido al piso de {y} es igual a {z // y}")

#Modulo cuando hacemos una division y tiene un numero que sobra, ese es el modulo 
print(f"{z} modulo de {y} es igual {z % y}")

#Potencia
print(f"{x} elevado a la potencia {y} es igual a {x ** y}")

#Raiz cuadrada
print(f"La raiz cuadrada de {x} es igual a {x ** 0.5}")
